// Helper functions
export const getExerciseById = (id: string): ExerciseDetail | null => {
  return exerciseDetails[id] || null;
};

export const getAllExerciseDetails = (): ExerciseDetail[] => {
  return Object.values(exerciseDetails);
};

export const getExercisesByCategory = (category: string): ExerciseDetail[] => {
  return Object.values(exerciseDetails).filter(
    (ex) => ex.category === category
  );
};

export const getExercisesByDifficulty = (
  difficulty: string
): ExerciseDetail[] => {
  return Object.values(exerciseDetails).filter(
    (ex) => ex.difficulty === difficulty
  );
};
